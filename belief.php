<h1>What we Believe</h1>
<h2>About the Bible</h2>
<p>We believe the Bible is God’s Holy Word which is entirely without error or contradiction in all matters of which it stands.  It consists of 66 books written by about 40 authors over 1500 years.  It reveals to us everything that God intends on us knowing about Him and His plans for mankind.  God has supernaturally inspired its original writings and will preserve it for mankind for all ages.  2 Timothy 3:16, 1 Peter 1:24-25, 2 Peter 1:21 </p>

<h2>About God</h2>
<p>We believe that there is one, and only one true God.  He eternally exists in three persons: Father, Son, and Holy Spirit. They are equal in essence and attribute, yet distinct in office and activity.  He is so powerful that He created the entire world in six, literal, 24 hour days.  He is so Holy and so loving, that He gave man a will to choose God.  When man chose to sin against God, He initiated His redemptive plan for mankind.  Genesis 1-3, Deuteronomy 6:4, Matthew 28:19, Romans 5:8</p>

<p>We believe the Father desires a relationship of holiness and love with mankind.  The only way this relationship can occur is through the gracious gift that He has given, Jesus Christ.  Genesis 3:8-9, John 3:16, Romans 8:15, Ephesians 2:8-9</p>

<p>We believe that Jesus Christ has existed eternally as God, and was made into the likeness of a man, born of a virgin, and died and rose again for the sins of the world in accordance with the will of God.  Isaiah 53, John 1:1-14, John 3:16</p>

<p>We believe that the Holy Spirit takes an active role in restraining evil in this age.  He also takes an active role in every true believer by convicting them of sin, helping them understand the Bible, and empowering them to live a life that pleases God.  John 16:8-15, Acts 1:8, Romans 8:14-16&amp;26-27</p>

<h2>About Mankind</h2>
<p>We believe that mankind has continually failed to live up to God’s standards.  This began when Adam chose to move from his sinless state to being sinful when he ate of the forbidden fruit.  God acted on behalf of mankind by making full payment for the sin of the world.  God identified Jesus’ coming sacrifice through the sacrificial system in the Old Testament.  God has made it possible for mankind to be right with God through faith in the death, burial, and resurrection of Christ. We believe in the eternal security of the believers.  Ephesians 2:1-3, 8-9, Romans 3:10&amp;23, 1 Corinthians 15:1-4,    John 10:28 </p>

<h2>About the Church</h2>
<p>We believe the church, which is the Body of Christ began on the day of Pentecost and has two divine institutions: The Lords Supper as a memorial, and Baptism by immersion.  The main purpose of the church is to reach the world as ambassadors of and for Christ.  This is made possible by genuine fellowship of the church and by using our God given gifts for His service.  Acts 2:1, 1 Corinthians 6:19-20, 12:12 -18, Matthew 28:19-20 </p>

<h2>About Giving to the Church</h2>
<p>We believe that Scriptural giving is a command of God to bring our gift to the church on the first day of the week.  This is an act of both obedience and grace by each believer in Christ.  This is done to care for expenses of the church operations and the needs of the church body, the pastoral staff, our missionaries, and the community.  Malachi 3:10, Acts 2:44-47; 4:34-37, I Corinthians 16:1-4,  II Corinthians 9 </p>

<h2>About Coming Events</h2>
<p>We believe that Christ may at any moment return in the air to rapture the saints and that a tribulation period of seven years will follow after which Christ will come to Earth with his saints to rule for a thousand years. After His millennial rule, the wicked will be judged and cast into the Lake of Fire. We believe in the eternal, conscious punishment of the lost, and the eternal joy of the saints.  1 Corinthians 15:51-57, 1 Thessalonians 4:13 -18, Revelation 19:11 &amp; 20:10-15</p>
